# it is common in langueges to have an "fprint" function
# in python you just put the f in the print statement
# in an fprint, you can insert varibles in {} straight into the string
# much easier


var1 = 5
var2 = 6777
var3 = 5.378
varList = ["apple", "bannana", "cookie"]


print(var1)

print("###")

print(var2)

print("###")

print("Hellow " + str(var2))
print(f"Hellow {var2}")

print("###")

print(f"This is a int: {var1} and this is a float: {var3}")
print(f"And this a list! {varList}")
 
